package data.models;

public class Visitor {
    private String name;
    private String accessCode;

    public String getName() {
        return name;
    }

    public String getAccessCode() {
        return accessCode;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAccessCode(String accessCode) {
        this.accessCode = accessCode;
    }
}